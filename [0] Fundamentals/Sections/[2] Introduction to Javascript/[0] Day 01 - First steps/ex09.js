/**
 9 - Use if/else to write a code that defines three numbers in variables and returns true if at least one of the three is odd.

Otherwise, the code should return false.
 */

let numberOne = 1109;
let numberTwo = 70;
let numberThree = 230;

if (numberOne % 2 !== 0 || numberTwo % 2 !== 0 || numberThree % 2 !== 0) {
  console.log(true);
} else {
  console.log(false);
}